type Language = "en" | "tr";
type TranslationKey = keyof typeof translations.en;

interface LanguageContextType {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (
    key: TranslationKey | (string & {}),
    options?: { [key: string]: string | number }
  ) => string;
  t_rarity: (rarity: DisplayRarity) => string;
  t_stat: (stat: ItemStat | "inventoryWeight") => string;
  t_slot: (slot: string) => string;
  t_armor_type: (armorType: string) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(
  undefined
);

export const LanguageProvider: FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>("tr");

  const t = (
    key: TranslationKey | (string & {}),
    options?: { [key: string]: string | number }
  ) => {
    let translation = (translations[language] as any)[key] || key;
    if (options) {
      Object.keys(options).forEach((optionKey) => {
        translation = translation.replace(
          `{{${optionKey}}}`,
          String(options[optionKey])
        );
      });
    }
    return translation;
  };

  const t_rarity = (rarity: DisplayRarity) => {
    return translations[language][rarity] || rarity;
  };

  const t_stat = (stat: ItemStat | "inventoryWeight") => {
    return translations[language][stat as ItemStat] || stat;
  };

  const slotKeyMap: Record<string, string> = {
    Zırh: "slot_zirh",
    Kask: "slot_kask",
    Eldiven: "slot_eldiven",
    Ayakkabı: "slot_ayakkabi",
    Küpe: "slot_kupe",
    Bilezik: "slot_bilezik",
    Yüzük: "slot_yuzuk",
    Kolye: "slot_kolye",
    Kılıç: "slot_kilic",
    Kalkan: "slot_kalkan",
    "Çift El Kılıç": "slot_cift_el_kilic",
    Mızrak: "slot_mizrak",
    "Çift Balta": "slot_cift_balta",
    "Çift Bıçak": "slot_cift_bicak",
    Yay: "slot_yay",
    Arbalet: "slot_arbalet",
    Asa: "slot_asa",
    "Yaşam Asa": "slot_yasam_asa",
    "Savaş Çekici": "slot_savas_cekici",
    Çanta: "slot_canta",
    Kazma: "slot_kazma",
    Orak: "slot_orak",
    Bıçak: "slot_bicak",
    Balta: "slot_balta",
    Olta: "slot_olta",
  };
  const t_slot = (slot: string) => {
    const key = slotKeyMap[slot];
    return t(key as TranslationKey) || slot;
  };

  const armorTypeKeyMap: Record<string, string> = {
    "[Ağır]": "armor_type_agir",
    "[Hafif]": "armor_type_hafif",
    "[Cübbe]": "armor_type_cubbe",
  };
  const t_armor_type = (armorType: string) => {
    const key = armorTypeKeyMap[armorType];
    return t(key as TranslationKey) || armorType;
  };

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      t,
      t_rarity,
      t_stat,
      t_slot,
      t_armor_type,
    }),
    [language]
  );

  return React.createElement(
    LanguageContext.Provider,
    { value: value },
    children
  );
};

export const useTranslation = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useTranslation must be used within a LanguageProvider");
  }
  return context;
};
